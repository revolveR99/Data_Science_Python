int	ft_strlen(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		a++;
	}
	return (a);
}

int	main(void)
{
	printf("a: %d", ft_strlen("Martin likes chicken with rice"));
}
